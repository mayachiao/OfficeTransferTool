package com.moonshaped.example.Datas;

import java.util.List;

public class OldData {

	String buildNo;
	List<String> owner;
	List<String> otherOwner;
	String measureMap;
	
	public String getBuildNo() {
		return buildNo;
	}
	public void setBuildNo(String buildNo) {
		this.buildNo = buildNo;
	}
	public List<String> getOwner() {
		return owner;
	}
	public void setOwner(List<String> owner) {
		this.owner = owner;
	}
	public List<String> getOtherOwner() {
		return otherOwner;
	}
	public void setOtherOwner(List<String> otherOwner) {
		this.otherOwner = otherOwner;
	}
	public String getMeasureMap() {
		return measureMap;
	}
	public void setMeasureMap(String measureMap) {
		this.measureMap = measureMap;
	}
	
}
